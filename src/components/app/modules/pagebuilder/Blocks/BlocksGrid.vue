<template>
  <div class="blocks-grid">
    <div  v-for="(block, index) in blocks"
          :style="block.template?block.template.style:''"
          :key="block.id" :class="'block-item block-'+block.layout"  >
        <span  class="block-items" >
          <ion-icon v-if="!deleted && block.s_order !== 1" @click="changeBlockOrder(false, block.id)" name="arrow-back-outline"></ion-icon>
          {{block.order}}-{{block.layout}}-layout
          <ion-icon v-if="!deleted" @click="openNewBlockModal(block)" name="create-outline"></ion-icon>
          <ion-icon v-if="!deleted"  @click="removeBlockConfirm(block.id)" name="trash-outline"></ion-icon>
          <ion-icon v-if="!deleted"  @click="openNewElementModal(null, block.id)" name="add-outline"></ion-icon>
          <ion-icon v-if="deleted"  @click="restoreBlockConfirm(block.id)" name="refresh-outline"></ion-icon>
          <ion-icon v-if="deleted"  @click="deleteBlockConfirm(block.id)" name="trash-outline"></ion-icon>
          <ion-icon
            v-if="!deleted && blocks[index+1] && blocks[index+1].s_order > block.s_order"
            @click="changeBlockOrder(true, block.id)"
            style="float: right"
            name="arrow-forward-outline"
          ></ion-icon>
        </span>
      <span
        class="element-items"
        :style="elem.template?'width:'+ elem.template.width:'width:100%'"
        v-for="elem in block.elements"
        :key="elem.id"
      >
            <span v-if="!deleted" class="element-icons" >s
              element - {{elem.type}}
              <ion-icon  @click="openNewElementModal(elem, block.id)" name="create-outline"></ion-icon>
              <ion-icon  @click="deleteElementConfirm(elem.id, block.id)" name="trash-outline"></ion-icon>
            </span>
          <div
            v-if="elem.type === 'html'"
            v-html="elem.content"
            :style="elem.template?elem.template.style:''"
          >
          </div>
           <img
             v-else-if="elem.type === 'image'"
             :style="elem.template?elem.template.style+'width:100%':''"
             :src="elem.images[0]?elem.images[0].url_original:''" width="100%"
           />
          <ycms-video-player
            v-else-if="elem.type === 'video'"
            :style="elem.template?elem.template.style+'width:100%':''"
            :src="elem.content">
          </ycms-video-player>
          <button
            v-else-if="elem.type === 'button'"
            :style="elem.template?elem.template.style+'width:100%':''"
            :href="JSON.parse(elem.content).button_link"
          >
            {{JSON.parse(elem.content).button_title}}
          </button>
          <iframe
            v-else-if="elem.type === 'iframe'"
            :style="elem.template?elem.template.style+'width:100%':''"
            :src="elem.content"
          >
          </iframe>
              <carousel
                v-else-if="elem.type === 'slider'"
                :autoplay="false"
                :data="getArray(elem)"
              >
              </carousel>

          <div :style="elem.template?elem.template.style+'width:'+elem.template.width:'height:100%;width:100%'" v-else-if="elem.type === 'map'" >
              <ycms-google-map
                :lat="JSON.parse(elem.content).lat+''"
                :lng="JSON.parse(elem.content).lng+''"
                :style_="elem.template?'width:'+elem.template.width+'; height:'+elem.template.height:'width:100%; height: 100%'"
              ></ycms-google-map>
          </div>
        </span>
      <span v-if="block.elements.length === 0">
          No elements found! <a v-if="!deleted" @click="openNewElementModal(null, block.id)">Create element</a>
      </span>
    </div>
  </div>
</template>
<script>
  export default {
    name: 'block-grid',
    props: {
      blocks: Array,
      deleted: Boolean,
    },
    data() {
      return {
      }
    },
    watch: {
      blocks: function (newv, oldv) {
        this.blocks = newv
      }
    },
    methods: {
      getArray(obj){
        let arr = [];
        let style;

        for(let i=0;i<obj.images.length;i++){
          style = obj.template?obj.template.style+'width:'+obj.template.width:'';
          arr.push('<img  style="'+style+'"  src="'+obj.images[i].url_original+'" />')
        }

        return arr;
      },
      goToPStep(st, template){
        if (template){
          this.style_ = template
        }
        this.p_step = st;
      },
      openNewBlockModal(block){
        this.$emit('open-block', block);
      },
      openNewElementModal(element, block_id){
        this.$emit('open-element', element, block_id);
      },
      restoreBlockConfirm(id){
        this.$emit('restore-block', id);
      },
      removeBlockConfirm(id){
        this.$emit('remove-block', id);
      },
      deleteBlockConfirm(id){
        this.$emit('delete-block', id);
      },
      deleteElementConfirm(id, block_id){
        this.$emit('delete-element', id, block_id);
      },
      changeBlockOrder(add, id){
        this.$emit('change-block-order', add, id);
      }
    },
    mounted() {
    }
  }
</script>

